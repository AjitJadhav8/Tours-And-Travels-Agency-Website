<script>
  function validateForm() {
    var name = document.forms[0].t1.value;
    var contactNo = document.forms[0].t2.value;
    var email = document.forms[0].t3.value;
    var message = document.forms[0].t4.value;

    // Check if any of the fields are empty
    if (name === '' || contactNo === '' || email === '' || message === '') {
      alert('Please fill in all fields.');
      return false;
    }

    // Validate name field: no special characters or numbers
    var nameRegex = /^[a-zA-Z ]+$/;
    if (!nameRegex.test(name)) {
      alert('Name field should only contain letters and spaces.');
      return false;
    }

    // Validate contact number field: only 10 to 12 digits allowed
    var contactNoRegex = /^\d{10,12}$/;
    if (!contactNoRegex.test(contactNo)) {
      alert('Contact No field should only contain 10 to 12 digits.');
      return false;
    }

    // Validate email field using regex
    var emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    if (!emailRegex.test(email)) {
      alert('Invalid email address.');
      return false;
    }

    return true; // Allow the form submission
  }
